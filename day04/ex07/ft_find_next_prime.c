int	ft_find_next_prime(int nb)
{
	int i;
	int mod;

	i = 1;
	if (nb == 0 || nb == 1 || nb < 0)
		return (2);
	while (i < (nb - 1))
	{
		i++;
		mod = nb % i;
		if (mod == 0)
		{
			nb++;
			i = 1;
		}
	}
	return (nb);
}
