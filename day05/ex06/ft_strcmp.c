int	ft_strcmp(char *s1, char *s2)
{
	int i;
	int num;

	i = 0;
	num = 0;
	while (s1[i] == s2[i] && s1[i] != '\0')
	{
		i++;
	}
	if (s1[i] != s2[i])
		num = s1[i] - s2[i];
	return (num);
}
